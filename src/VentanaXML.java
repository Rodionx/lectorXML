    public int abrir_XML_DOM (File fichero){
        doc=null;
        
        try{
            DocumentBuilderFactory factory = DocumentBuilderFactory.newInstance();
            factory.setIgnoringComments(true);
            factory.setIgnoringElementContentWhitespace(true);
            DocumentBuilder builder = factory.newDocumentBuilder();
            doc = builder.parse(fichero);
            return 0;
            
        }catch(Exception e){
            return -1;
        }
    }
    
    private void seleccionarFichero(){
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        if (JFileChooser.APPROVE_OPTION == fileChooser.showOpenDialog(jLabel1)) {
            File a = fileChooser.getSelectedFile();
            archivoSeleccionado = a.getAbsolutePath();
        }
        jLabel1.setText(archivoSeleccionado);
    }
